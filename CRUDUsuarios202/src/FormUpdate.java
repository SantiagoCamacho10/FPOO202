    private void btn_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ActualizarActionPerformed
        JOptionPane.showMessageDialog(this,"Estas seguro de continuar?", "Verificador", JOptionPane.INFORMATION_MESSAGE);
        // 1. Obtener los valores de los txt
        String id = jTextField1.getText();
        Integer ID = Integer.valueOf(id);
        String nombre = jTextField2.getText();
        String correo = jTextField3.getText();
        String contrasena = jTextField4.getText();
        
        // 2. Validar que no envie vacios a BD
        if(nombre.isEmpty() || correo.isEmpty() || contrasena.isEmpty()){
            JOptionPane.showMessageDialog(this,"Todos los campos son obligatorios","Error", JOptionPane.ERROR);
            return;
        }
        
        // 3. Ejecutar el update
        UserCRUD crud = new UserCRUD();
        boolean status = crud.editarusuario(ID, nombre, correo, contrasena);
    }//GEN-LAST:event_btn_ActualizarActionPerformed
