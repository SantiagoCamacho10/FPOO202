//Escribir un programa que visualice en pantalla los números múltiplos de 10 comprendidos entre 10 y 1000
public class Multiplos_de_10 {
    public static void main(String[] args) {
        System.out.println("Números múltiplos de 10 entre 10 y 1000:");
        for (int i = 10; i <= 1000; i += 10) {
            System.out.println(i);
        }
    }
}
